public class Paciente {
    String nombre;
    String apellido;
    String telefono;
    boolean estaVivo;

    public Paciente(String nombre, String apellido, String telefono, boolean estaVivo) {
        this.nombre = nombre;
        this.apellido = apellido;
        this.telefono = telefono;
        this.estaVivo = estaVivo;
    }
}
